#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*mapped;
	t_list	*temp;
	void	*content;

	mapped = 0;
	while (lst)
	{
		content = f(lst->content);
		temp = ft_lstnew(content);
		if (!temp)
		{
			del(content);
			ft_lstclear(&mapped, del);
			return (0);
		}
		ft_lstadd_back(&mapped, temp);
		lst = lst->next;
	}
	return (mapped);
}
